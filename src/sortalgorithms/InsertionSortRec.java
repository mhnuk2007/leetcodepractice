package sortalgorithms;

import java.util.Arrays;

public class InsertionSortRec {
    public static void main(String[] args) {
        int[] arr = {64, 34, 45, 65, 2, 23, 2};
        System.out.println("Original array:" + Arrays.toString(arr));
        insertionSort(arr, arr.length);
        System.out.println("Insertion Sort: " + Arrays.toString(arr));
    }

    private static void insertionSort(int[] arr, int n) {
        if (n <= 1) return;
        insertionSort(arr, n - 1);
        int key = arr[n - 1];
        insert(arr, n - 2, key);
    }
    // n = 1,2,3,4,5,6,7 -> 7->6->5->4->3->2 (Top)
    // 64, 34, 45, 65, 2, 23, 2
    // n = 2, i = 0,  key = 34 -> 64,64,...
    // n = 2, i = -1, key = 34 -> 34, 64,...
    // n = 3, i = 1,  key = 45 -> 34, 64, 64, ...
    // n = 3, i = 0,  key = 45 -> 34, 45, 64, 65, ...
    // n = 4, i = 2,  key = 65 -> 34, 45, 64, 65,  2, ...
    // n = 5, i = 3,  key = 2  -> 34, 45, 64, 65, 65, ...
    // n = 5, i = 2,  key = 2  -> 34, 45, 64, 64, 65, ...
    // n = 5, i = 1,  key = 2  -> 34, 45, 45, 64, 65, ...
    // n = 5, i = 0,  key = 2  -> 34, 34, 45, 64, 65, ...
    // n = 5, i = -1, key = 2  -> 2, 34, 45, 64, 65, 23, ...
    // n = 6, i = 4,  key = 23 -> 2, 34, 45, 64, 65, 65, ...
    // n = 6, i = 3,  key = 23 -> 2, 34, 45, 64, 64, 65, ...
    // n = 6, i = 2,  key = 23 -> 2, 34, 45, 45, 64, 65, ...
    // n = 6, i = 1,  key = 23 -> 2, 34, 34, 45, 64, 65, ...
    // n = 6, i = 0,  key = 23 -> 2, 23, 34, 45, 64, 65, 2, ...
    // n = 7, i = 5,  key = 2  -> 2, 23, 34, 45, 64, 65, 65, ...
    // n = 7, i = 4,  key = 2  -> 2, 23, 34, 45, 64, 64, 65, ...
    // n = 7, i = 3,  key = 2  -> 2, 23, 34, 45, 45, 64, 65, ...
    // n = 7, i = 2,  key = 2  -> 2, 23, 34, 34, 45, 64, 65,  ...
    // n = 7, i = 1,  key = 2  -> 2, 23, 23, 34, 45, 64, 65,  ...
    // n = 7, i = 0,  key = 2  -> 2,  2, 23, 34, 45, 64, 65,  ...

    private static void insert(int[] arr, int i, int key) {
        if (i < 0 || arr[i] <= key) {
            arr[i + 1] = key;
            return;
        }
        arr[i + 1] = arr[i];
        insert(arr, i - 1, key);
    }
}
